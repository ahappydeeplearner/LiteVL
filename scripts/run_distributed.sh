#!/bin/bash
# ============================================================
# 分布式训练脚本 (多卡 DDP)
# ============================================================

set -e

STAGE=${1:-"pretrain"}
NUM_GPUS=${2:-2}

echo "=========================================="
echo "  分布式训练: ${STAGE} (${NUM_GPUS} GPUs)"
echo "=========================================="

torchrun --nproc_per_node=${NUM_GPUS} \
    --master_port=29500 \
    train.py --stage ${STAGE}
